# I can add a single element to a list using the .append() Python method.

orders = ["fries", "cheese burger"]
orders.append("chicken nuggets")
orders.append("milkshake")

print(orders)

# Adding an element to an empty list.

car = []

car.append("transmission")
print(car)

# Adding an element to a list with other elements.
# This will add the new element to the end of the list.

fordF450Truck = ["roof", "engine", "suspensions", "tires"]

fordF450Truck.append("tinted windows")
fordF450Truck.append("transmission")
fordF450Truck.append("muffler")
print(fordF450Truck)