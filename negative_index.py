# To select the last element of my list, I can
# use the index -1.

batFamily = ["Batman", "Robin", "Batgirl", "Nightwing", "Red Hood", "Batwoman"]

lastElement = batFamily[-1]
index5Element = batFamily[5]

print(lastElement)
print(index5Element)

